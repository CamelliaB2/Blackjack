    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	BlackJackGame mygame = new BlackJackGame();

		mygame.initializeGame();
		do {
			mygame.shuffle();
			mygame.getBets();
			mygame.dealCards();
			mygame.printStatus();
			mygame.checkBlackjack();
			mygame.hitOrStand();
			mygame.dealerPlays();
			mygame.settleBets();
			mygame.printMoney();
			mygame.clearHands();
			mygame.endGame();
		} while (mygame.playAgain());
    }
    
    

